#include <mlx_math.h>

void	mx_copy(t_mx4 *m1, t_mx4 m2)
{
	int	i;

	i = -1;
	while (++i < 4)
	{
		m1->x[i] = m2.x[i];
		m1->y[i] = m2.y[i];
		m1->z[i] = m2.z[i];
		m1->t[i] = m2.t[i];
	}
}
